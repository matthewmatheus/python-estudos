

print("Esse programa calcula a velocidade media de um patinete")

distancia = input("Distancia em metros percorrida pelo patinete: ")

tempo = input("Quantidade em minutos de tempo que o patinete demorou para percorrer a distancia: ")

velocidade_media = int(distancia) / int(tempo)

print("O patinete atingiu uma velocidade de {0:.2f} m/min".format(velocidade_media))

