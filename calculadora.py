
valor1 = input("Informe o primeiro valor: ")

valor2 = input("Informe o segundo valor: ")

soma = int(valor1) + int(valor2)

print("A soma é: ", soma)

subtracao = int(valor1) - int(valor2)

print("A subtracao é: ", subtracao)

divisao = int(valor1) / int(valor2)

print("A divisão é: ", divisao)

multiplicacao = int(valor1) * int(valor2)

print("A multiplicacao é: ", multiplicacao)

print("fim :)")